#include <unistd.h>

int	main(int ac, char **av)
{
	int i;
	int j;

	i = 0;
	while (av[j] != NULL)
	{
		i = 0;
		while (*av[i])
		{
			write(1, &(*av), 1);
			i++;
		}
		j++;
	}
	return (0);
}
